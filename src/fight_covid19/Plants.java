package fight_covid19;

public class Plants extends D_object{
	private int HP = 0;
	private int ATK = 0;
	private int Cost = 0;
	private int ATK_speed = 0;
	
	public int getHP() {
		return HP;
	}
	public void setHP(int HP) {
		this.HP = HP;
	}
	
	public int getATK() {
		return ATK;
	}
	public void setATK(int ATK) {
		this.ATK = ATK;
	}
	
	public int getCost() {
		return Cost;
	}
	public void setCost(int Cost) {
		this.Cost = Cost;
	}
	
	public int getATK_speed() {
		return ATK_speed;
	}
	public void setATK_speed(int ATK_speed) {
		this.ATK_speed = ATK_speed;
	}
	
}
